private void tampilkanDataPribadi() {
    try (Connection conn = HRD.DatabaseConnection.connect()) {
        String sql = "SELECT * FROM pegawai WHERE ID_Pegawai = ?";
        PreparedStatement ps = conn.prepareStatement(sql);
        ps.setInt(1, Pegawai.Session_Pegawai.idPegawai); // ID dari session login

        ResultSet rs = ps.executeQuery();

        if (!rs.next()) {
            JOptionPane.showMessageDialog(this, "⚠️ Data tidak ditemukan.");
            dispose(); // keluar dari form
            return;
        }

        String ttl = rs.getString("Tempat_Tanggal_Lahir");
        String jenisKelamin = rs.getString("Jenis_Kelamin");

        jTextField1.setText(rs.getString("Nama_Pegawai"));
        jTextField2.setText(ttl);
        jTextField3.setText(rs.getString("Alamat"));
        jComboBox1.setSelectedItem(jenisKelamin);
        jTextField5.setText(rs.getString("Email"));
        jTextField6.setText(rs.getString("No_HP"));
        jTextField7.setText(rs.getString("Jabatan"));
        jTextField4.setText(String.valueOf(rs.getInt("Gaji_Pokok")));
        jTextField8.setText(rs.getString("Status_Pegawai"));

        // Hanya kolom tertentu yang boleh diedit
        jTextField1.setEditable(false);
        jTextField7.setEditable(false);
        jTextField8.setEditable(false);
        jTextField4.setEditable(false);

        jTextField2.setEditable(ttl == null || ttl.trim().isEmpty());
        jComboBox1.setEnabled(jenisKelamin == null || jenisKelamin.trim().isEmpty());

        jTextField3.setEditable(true);
        jTextField5.setEditable(true);
        jTextField6.setEditable(true);

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "❌ Gagal menampilkan data pribadi: " + e.getMessage());
    }
}




