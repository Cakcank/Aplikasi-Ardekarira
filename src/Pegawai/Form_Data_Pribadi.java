    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String ttl = jTextField2.getText().trim();
        String alamat = jTextField3.getText().trim();
        String jk = jComboBox1.getSelectedItem().toString();
        String email = jTextField5.getText().trim();
        String nohp = jTextField6.getText().trim();

        if (ttl.isEmpty() || alamat.isEmpty() || jk.equals("-- Pilih Jenis Kelamin --") || email.isEmpty() || nohp.isEmpty()) {
            JOptionPane.showMessageDialog(this, "❗ Semua field harus diisi dengan benar.");
            return;
        }

        try (Connection conn = HRD.DatabaseConnection.connect()) {
            String sql = "UPDATE pegawai SET Tempat_Tanggal_Lahir = ?, Alamat = ?, Jenis_Kelamin = ?, Email = ?, No_HP = ? WHERE ID_Pegawai = ?";
            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setString(1, ttl);
            ps.setString(2, alamat);
            ps.setString(3, jk);
            ps.setString(4, email);
            ps.setString(5, nohp);
            ps.setInt(6, Pegawai.Session_Pegawai.idPegawai);
            ps.executeUpdate();

            JOptionPane.showMessageDialog(this, "✅ Data pribadi berhasil disimpan.");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "❌ Gagal menyimpan data: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

