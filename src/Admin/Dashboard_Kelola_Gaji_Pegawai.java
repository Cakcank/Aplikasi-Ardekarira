    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
            String keyword = jTextField8.getText().trim();

            DefaultTableModel model = new DefaultTableModel();
            model.addColumn("No");
            model.addColumn("ID");
            model.addColumn("Username");
            model.addColumn("Akses");
            model.addColumn("Jabatan");
            model.addColumn("Status");
            model.addColumn("Gaji");

            try (Connection conn = DatabaseConnection.connect()) {
                String sql = "SELECT * FROM users WHERE Akses = 'pegawai' ORDER BY ID";
                PreparedStatement ps = conn.prepareStatement(sql);
                ResultSet rs = ps.executeQuery();

                int no = 1;
                boolean ada = false;

                while (rs.next()) {
                    String username = rs.getString("Username");

                    if (keyword.isEmpty() || username.toLowerCase().contains(keyword.toLowerCase())) {
                        model.addRow(new Object[]{
                            no,
                            rs.getInt("ID"),
                            username,
                            rs.getString("Akses"),
                            rs.getString("Jabatan"),
                            rs.getString("Status"),
                            rs.getInt("Gaji")
                        });
                        ada = true;
                    }
                    no++;
                }

                if (!ada) {
                    JOptionPane.showMessageDialog(null, "üîç Data tidak ditemukan!");
                    tampilkanDataPegawai();
                }

                jTable1.setModel(model);

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "‚ùå Gagal mencari data: " + e.getMessage());
            }
    }//GEN-LAST:event_jButton3ActionPerformed

