    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String id = jTextField1.getText().trim();
        String gajiBaru = jTextField5.getText().trim();

        // Validasi
        if (id.isEmpty() || gajiBaru.isEmpty()) {
            JOptionPane.showMessageDialog(this, "‚ö†Ô∏è Silakan pilih data dari tabel dan isi gaji terlebih dahulu!");
            return;
        }

        int gaji;
        try {
            gaji = Integer.parseInt(gajiBaru);
            if (gaji < 0) {
                JOptionPane.showMessageDialog(this, "‚ùå Gaji tidak boleh negatif!");
                return;
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "‚ùå Gaji harus berupa angka!");
            return;
        }

        // Update ke database
        try (Connection conn = DatabaseConnection.connect()) {
            String sql = "UPDATE users SET Gaji = ? WHERE ID = ?";
            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setInt(1, gaji);
            ps.setInt(2, Integer.parseInt(id));
            int hasil = ps.executeUpdate();

            // Update juga ke tabel pegawai
            String updatePegawai = "UPDATE pegawai SET Gaji_Pokok = ? WHERE ID_Pegawai = ?";
            PreparedStatement ps2 = conn.prepareStatement(updatePegawai);
            ps2.setInt(1, gaji);
            ps2.setInt(2, Integer.parseInt(id));
            ps2.executeUpdate();

            ps.close();
            ps2.close();

            if (hasil > 0) {
                JOptionPane.showMessageDialog(this, "‚úÖ Gaji berhasil diperbarui");
                tampilkanDataPegawai(); // refresh tabel
            } else {
                JOptionPane.showMessageDialog(this, "‚ö†Ô∏è Gagal memperbarui gaji.");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "‚ùå Gagal mengupdate gaji: " + e.getMessage());
        }

    }//GEN-LAST:event_jButton1ActionPerformed

